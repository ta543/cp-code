using Z = MInt<P>;
struct SuffixArray {
    int n;
    std::vector<int> sa, rk, lc;
    SuffixArray(const std::string &s) {
        n = s.length();
        sa.resize(n);
        lc.resize(n - 1);
        rk.resize(n);
        std::iota(sa.begin(), sa.end(), 0);
        std::sort(sa.begin(), sa.end(), [&](int a, int b) {return s[a] < s[b];});
        rk[sa[0]] = 0;
        for (int i = 1; i < n; ++i)
            rk[sa[i]] = rk[sa[i - 1]] + (s[sa[i]] != s[sa[i - 1]]);
        int k = 1;
        std::vector<int> tmp, cnt(n);
        tmp.reserve(n);
        while (rk[sa[n - 1]] < n - 1) {
            tmp.clear();
            for (int i = 0; i < k; ++i)
                tmp.push_back(n - k + i);
            for (auto i : sa)
                if (i >= k)
                    tmp.push_back(i - k);
            std::fill(cnt.begin(), cnt.end(), 0);
            for (int i = 0; i < n; ++i)
                ++cnt[rk[i]];
            for (int i = 1; i < n; ++i)
                cnt[i] += cnt[i - 1];
            for (int i = n - 1; i >= 0; --i)
                sa[--cnt[rk[tmp[i]]]] = tmp[i];
            std::swap(rk, tmp);
            rk[sa[0]] = 0;
            for (int i = 1; i < n; ++i)
                rk[sa[i]] = rk[sa[i - 1]] + (tmp[sa[i - 1]] < tmp[sa[i]] || sa[i - 1] + k == n || tmp[sa[i - 1] + k] < tmp[sa[i] + k]);
            k *= 2;
        }
        for (int i = 0, j = 0; i < n; ++i) {
            if (rk[i] == 0) {
                j = 0;
            } else {
                for (j -= j > 0; i + j < n && sa[rk[i] - 1] + j < n && s[i + j] == s[sa[rk[i] - 1] + j]; )
                    ++j;
                lc[rk[i] - 1] = j;
            }
        }
    }
};

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);
    
    int n, k;
    std::cin >> n >> k;
    
    std::string s;
    std::cin >> s;
    
    std::string t = s;
    
    int res = k;
    int r = n;
    for (int i = 0; i < n; i++) {
        if (t[i] == '1') {
            while (r > 0 && t[r - 1] == '1') {
                r--;
            }
            if (res > 0 && i < r) {
                std::swap(t[i], t[r - 1]);
                res--;
            }
        }
    }
    
    auto work = [&](int k) {
        int cnt = std::count(s.begin(), s.end(), '1');
        int need = std::max(1, cnt - k);
        std::vector<int> p;
        for (int i = 0; i < n; i++) {
            if (s[i] == '1') {
                p.push_back(i);
            }
        }
        int len = n;
        for (int i = need - 1; i < p.size(); i++) {
            len = std::min(len, p[i] - p[i - (need - 1)] + 1);
        }
        if (len <= cnt) {
            t = std::string(n - cnt, '0') + std::string(cnt, '1');
            return;
        }
        std::vector<int> pre(n + 1);
        SuffixArray sa(s);
        for (int i = 0; i < n; i++) {
            pre[i + 1] = pre[i] + s[i] - '0';
        }
        for (auto i : sa.sa) {
            if (i + len <= n && pre[i + len] - pre[i] >= need) {
                std::string ans = s.substr(i, len);
                int res = k;
                for (int i = ans.size() - 1; i >= 0; i--) {
                    if (ans[i] == '0' && res > 0) {
                        res--;
                        ans[i] = '1';
                    }
                }
                ans = std::string(n - len, '0') + ans;
                t = std::min(t, ans);
                return;
            }
        }
        assert(false);
    };
    
    if (k >= 2) {
        work(k - 2);
    }
    if (k >= 1) {
        std::reverse(s.begin(), s.end());
        work(k - 1);
    }
    Z ans = 0;
    for (auto c : t) {
        ans = ans * 2 + (c - '0');
    }
    std::cout << ans << "\n";
    
    return 0;
}